//use it when know how many times you want to loop through a block of code
// step-init,condition,plus
#include <iostream>

int main(int argc, char const *argv[])
{
    for(int i=1;i<=10;i++)
    {
        std::cout<<"For Loop "<<i<<"# Times."<<std::endl;
    }
    std::cin.get();
    return 0;
}
